    private void btnValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValiderActionPerformed
        // TODO add your handling code here:
        int feuillet = Integer.parseInt(spNbFeuillets.getValue().toString());
        String nomArticle = txtNomArticle.getText();
        String nomPigiste = cboPigistes.getSelectedItem().toString();
        String nomMagasine = cboMagazines.getSelectedItem().toString();
        // A vous de jouer
        if(txtNomArticle.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Saisir un nom d'article","Erreur de saisie",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            // On crée la tâche
            Article leArticle = new Article(txtNomArticle.getText(), feuillet);
            // On teste si le thème n'est pas présent : dans ce cas il faut tout créer !
            if (!mesInformations.containsKey(nomPigiste))
            {
                ArrayList<Article> lesArticles = new ArrayList<>();
                lesArticles.add(leArticle);
                TreeMap<String, ArrayList<Article>> lesMagazine = new TreeMap<>();
                lesMagazine.put(nomMagasine, lesArticles);
                mesInformations.put(nomPigiste, lesMagazine);
            }
            // Le thème est présent mais il faut vérifier si le projet existe
            else if (!mesInformations.get(nomPigiste).containsKey(nomMagasine)){
                ArrayList<Article> lesArticles = new ArrayList<>();
                lesArticles.add(leArticle);
                mesInformations.get(nomPigiste).put(nomMagasine, lesArticles);
            }
            else
            {
                // le projet est présent donc il y a déjà des tâches pour ce projet
                // Il suffit d'ajouter la nouvelle tâche
                mesInformations.get(nomPigiste).get(nomMagasine).add(leArticle);
            }
            RemplirEdition();
            RemplirPigiste();
        }
    }//GEN-LAST:event_btnValiderActionPerformed

