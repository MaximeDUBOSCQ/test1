        double Alison = 1.89;
        double salaireAlison = 0;
        double Fortin = 1.76;
        double salaireFortin = 0;
        double Garnier = 1.33;
        double salaireGarnier = 0;
        double salaire = 0;
    
        public void RemplirEdition()
    {
        racine.removeAllChildren();
        for (String pigiste : mesInformations.keySet())
        {
            noeudNomPigiste = new DefaultMutableTreeNode(pigiste);
            for (String magazine : mesInformations.get(pigiste).keySet())
            {
                noeudNomMagazine = new DefaultMutableTreeNode(magazine);
                for (Article unArticle : mesInformations.get(pigiste).get(magazine))
                {
                    noeudArticle = new DefaultMutableTreeNode(unArticle.getTitreArticle());
                    noeudNomMagazine.add(noeudArticle);
                    noeudArticle = new DefaultMutableTreeNode(unArticle.getNbFeuillets());
                    noeudNomMagazine.add(noeudArticle);
                }
                noeudNomPigiste.add(noeudNomMagazine);
            }
            racine.add(noeudNomPigiste);
        }
        model = new DefaultTreeModel(racine);
        trEditions.setModel(model);
        for(int i = 0 ; i < trEditions.getRowCount() ; i++)
        {
            trEditions.expandRow(i);
        }
    }

        public void RemplirPigiste()
    {
        racine2.removeAllChildren();
        for (String pigiste : mesInformations.keySet())
        {
            noeudNomPigiste = new DefaultMutableTreeNode(pigiste);
            for (String magazine : mesInformations.get(pigiste).keySet())
            {
                noeudNomMagazine = new DefaultMutableTreeNode(magazine);
                for (Article unArticle : mesInformations.get(pigiste).get(magazine))
                {
                    if(pigiste == "Alison") {
                        salaireAlison = salaireAlison + Alison*unArticle.getNbFeuillets();
                        salaire = salaireAlison;
                    }
                    else if (pigiste == "Fortin"){
                        salaireFortin = salaireFortin +  Fortin*unArticle.getNbFeuillets();
                        salaire = salaireFortin;
                    }
                    else if (pigiste == "Garnier"){
                        salaireGarnier = salaireGarnier + Garnier*unArticle.getNbFeuillets();
                        salaire = salaireGarnier;
                    }
                    noeudCA = new DefaultMutableTreeNode(pigiste+ " : " + salaire);
                }
            }
            racine2.add(noeudCA);
        }
        model2 = new DefaultTreeModel(racine2);
        trPigistes.setModel(model2);
        for(int i = 0 ; i < trPigistes.getRowCount() ; i++)
        {
            trPigistes.expandRow(i);
        }
    }
        
